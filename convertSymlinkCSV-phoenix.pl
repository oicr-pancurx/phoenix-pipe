			{
				$seqType = "rna";
			}
			elsif ($libSplit[6] eq "MR")
			{
				$seqType = "rna";
			}
			else
			{
				$seqType = "other";
			}
		}
		else
		{
			$seqType = "other";
		}

		$groupID = "NoGroupID";
		if ($row{"Parent Sample Attributes"} =~ /geo_group_id.*?=(.*?);/)
		{
			$groupID = $1;
		}



		# correct metadata and skip if necessary

		if (exists $skipLane{"$library,$run,$lane,$barcode"})
		{
			$doLink = 0;
		}

		# skip miseq runs
		if (($run =~ /_M00753_/) or ($run =~ /_M00146_/))
		{
			$doLink = 0;
		}

		# skip runs
		if (exists $skipRuns{$run})
		{
			$doLink = 0;
		}

		if (exists $fixDonors{$donor})
		{
			$donor = $fixDonors{$donor};
		}

		if (exists $fixLibName{$library})
		{
			($donor,$sample,$groupID,$library) = split(/,/, $fixLibName{$library});
		}

		if (exists $fixLibGroup{$library})
		{
			$groupID = $fixLibGroup{$library};
		}

		if (exists $fixLaneSwap{"$library,$run,$lane,$barcode"})
		{
			($donor,$sample,$groupID,$library,$barcode,$seqType) = split(/,/, $fixLaneSwap{"$library,$run,$lane,$barcode"});
		}



		unless ($row{"Workflow Name"} eq "Xenome")
		{
			if (($filePath =~ /.*\.fastq\.gz$/) or ($filePath =~ /.*_sequence\.txt\.gz$/) or ($filePath =~ /.*_sequence\.txt$/))
			{
				if ($filePath =~ /.*R(.)_.*?\.fastq\.gz/)
				{
					$readNum = $1;
					$format = "sanger";
				}
				elsif ($filePath =~ /.*s_(.)_(.)_sequence\.txt\.gz$/)
				{
					$readNum = $2;
					$format = "illumina";
				}
				elsif ($filePath =~ /.*s_(.)_sequence\.txt\.gz$/)
				{
					$readNum = "1";
					$format = "illumina";
				}
				elsif ($filePath =~ /.*s_(.)_(.)_sequence\.txt$/)
				{
					$readNum = $2;
					$format = "illumina no zip";
				}
				elsif ($filePath =~ /.*s_(.)_sequence\.txt$/)
				{
					$readNum = "1";
					$format = "illumina no zip";
				}
				else
				{
					die "Couldn't parse read number from fastq [$filePath].  You'll need to update the regex in the script.\n";
				}



				# check for duplicates
				unless (exists $iusHash{"$run\t$lane\t$barcode\t$readNum\t$library"})
				{
					$iusHash{"$run\t$lane\t$barcode\t$readNum\t$library"} = $filePath;
				}
				else	# duplicate!
				{
					$doLink = 0;
					print "Skipping duplicate fastq for $donor $sample $seqType [$filePath]\n";
				}

				# check file exists and contains data
				if (-e $filePath)
				{
					$fileSize = -s $filePath;
					if ($fileSize < 100)
					{
						$doLink = 0;
						print "Skipping empty fastq for $donor $sample $seqType [$filePath]\n";
					}
				}
				else
				{
					$doLink = 0;
					print "Skipping non-existant fastq for $donor $sample $seqType [$filePath]\n";
				}
	


				if ($doLink == 1)
				{
					# create directory
					$samplePath = $sample;
					if ($groupID ne "NoGroupID")
					{
						$samplePath .= "_$groupID";
					}
					unless (-d "$donor/$samplePath/$seqType/fastq/")
					{
						unless ($testMode == 1)
						{
							`mkdir -p $donor/$samplePath/$seqType/fastq/`;
						}
					}
	

					# create file name
					if ($format eq "sanger")
					{
						$basename = "SWID_${swidIUS}_${library}_${run}_${barcode}_L00${lane}_R${readNum}_001.fastq.gz";
						$basenameNoIndex = "SWID_${swidIUS}_${library}_${run}_NoIndex_L00${lane}_R${readNum}_001.fastq.gz";
					}
					elsif ($format eq "illumina")
					{
						$basename = "SWID_${swidIUS}_${library}_${run}_${barcode}_L00${lane}_R${readNum}_sequence.txt.gz";
						$basenameNoIndex = "SWID_${swidIUS}_${library}_${run}_NoIndex_L00${lane}_R${readNum}_sequence.txt.gz";
					}
					else	# illumina no zip
					{
						$basename = "SWID_${swidIUS}_${library}_${run}_${barcode}_L00${lane}_R${readNum}_sequence.txt";
						$basenameNoIndex = "SWID_${swidIUS}_${library}_${run}_NoIndex_L00${lane}_R${readNum}_sequence.txt";
					}



					unless (-e "$donor/$samplePath/$seqType/fastq/$basename")
					{
						unless (-e "$donor/$samplePath/$seqType/fastq/$basenameNoIndex")		# seqware file names will say NoIndex for barcoded samples that are alone in their lane
						{
							unless ($testMode == 1)
							{
								`ln -s $filePath $donor/$samplePath/$seqType/fastq/$basename`;
							}
							print "Created new fastq in $donor/$samplePath/$seqType \"$basename\" [$filePath]\n";

						}
					}
				}

			}
		}
	}
}



