while ($l = <STDIN>)
{
	if ($processedHeader == 0)
	{
		chomp $l;
		@f = split(/\t/, $l);

		@headers = @f;

		$processedHeader = 1;
	}
	else
	{
		$doLink = 1;

		chomp $l;
		@f = split(/\t/, $l);

		for (my $i = 0; $i < scalar(@headers); $i++)
		{
			$row{$headers[$i]} = $f[$i];
		}


		for my $parent (split(/:/, $row{"Parent Sample Name"}))
		{
			unless ($parent eq "")
			{
				$donor = $parent;
			}
		}
